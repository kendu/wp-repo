###############################---WORKDIR---####################################

cd $( dirname $0 )

################################################################################

##############################---FUNCTIONS---###################################

function fixPermissions() {
    sudo find wp/ -type d -exec chmod 775 {} \;
    sudo find wp/ -type f -exec chmod 664 {} \;
    sudo chown -R $( id -u ${USER} ) wp/
}

################################################################################

###############################---EXECUTION---##################################

#Bring containers UP
docker-compose up -d --no-recreate

fixPermissions

